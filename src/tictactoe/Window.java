    public void press(int index) {
        if (lbs[index - 1].getText().equals("")) {
            lbs[index - 1].setText(turn);
            if (turn == "X") {
                lbs[index - 1].setBackground(Color.green);
            } else {
                lbs[index - 1].setBackground(Color.red);
            }
            changeTurn();
        }
        if (checkWinner()) {
            for (int i = 0; i < lbs.length; i++){
                if (lbs[i].getText().equals("")){
                    lbs[i].setText(" ");
                }
            }
        }
    }
    
    public void changeTurn() {
        if (turn == "X")
            turn = "O";
        else
            turn = "X";
    }
    
    public boolean checkWinner() {
        for (int i = 0; i < victories.length; i++){
            if (lbs[victories[i][0] - 1].getText().equals("X") &&
                    lbs[victories[i][1] - 1].getText().equals("X") &&
                    lbs[victories[i][2] - 1].getText().equals("X")) {
                JOptionPane.showMessageDialog(null, "X Wins!!");
                increaseGreenCounter();
                return true;
            }
            if (lbs[victories[i][0] - 1].getText().equals("O") &&
                    lbs[victories[i][1] - 1].getText().equals("O") &&
                    lbs[victories[i][2] - 1].getText().equals("O")) {
                JOptionPane.showMessageDialog(null, "O Wins!!");
                increaseRedCounter();
                return true;
            }
        }
        return false;
    }
    
    public void increaseGreenCounter() {
        int contador = Integer.parseInt(greenCounter.getText());
        contador++;
        greenCounter.setText(String.valueOf(contador));
    }
    
    public void increaseRedCounter() {
        int contador = Integer.parseInt(redCounter.getText());
        contador++;
        redCounter.setText(String.valueOf(contador));
    }
    
